public class CashList {
    private String price;
    private String title;
    private String content;
    private String useDate;

    public CashList(String price, String title, String content, String useDate) {
        this.price = price;
        this.title = title;
        this.content = content;
        this.useDate = useDate;
    }

    public String getPrice() {
        return price;
    }

    public CashList setPrice(String price) {
        this.price = price;
        return this;
    }

    public String getTitle() {
        return title;
    }

    public CashList setTitle(String title) {
        this.title = title;
        return this;
    }

    public String getContent() {
        return content;
    }

    public CashList setContent(String content) {
        this.content = content;
        return this;
    }

    public String getUseDate() {
        return useDate;
    }

    public CashList setUseDate(String useDate) {
        this.useDate = useDate;
        return this;
    }
}
