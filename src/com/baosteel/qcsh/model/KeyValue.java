package com.baosteel.qcsh.model;

/**
 * 键值对
 * @author 刘远祺
 *
 * @todo TODO
 *
 * @date 2015-9-9
 */
public class KeyValue<K,V> {
	
	/**键**/
	private K key;
	
	/**值**/
	private V value;
	
	public KeyValue(K k, V v){
		this.key = k;
		this.value = v;
	}

	public K getKey() {
		return key;
	}

	public void setKey(K k) {
		this.key = k;
	}

	public V getValue() {
		return value;
	}

	public void setValue(V v) {
		this.value = v;
	}
	
	
}	
