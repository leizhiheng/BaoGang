package com.baosteel.qcsh.model;

import java.io.Serializable;

/**
 * Created by jws on 2015/9/12.
 * 收货地址信息
 */
public class ConsigneeInfo implements Serializable{
    private String id;
    private String name;
    private String address;
    private String phone;
    private boolean isCheck;

    public ConsigneeInfo(String id, String name, String address, String phone, boolean isCheck) {
        this.id = id;
        this.name = name;
        this.address = address;
        this.phone = phone;
        this.isCheck = isCheck;
    }

    public String getId() {
        return id;
    }

    public ConsigneeInfo setId(String id) {
        this.id = id;
        return this;
    }

    public ConsigneeInfo(){}

    public String getName() {
        return name;
    }

    public ConsigneeInfo setName(String name) {
        this.name = name;
        return this;
    }

    public String getAddress() {
        return address;
    }

    public ConsigneeInfo setAddress(String address) {
        this.address = address;
        return this;
    }

    public String getPhone() {
        return phone;
    }

    public ConsigneeInfo setPhone(String phone) {
        this.phone = phone;
        return this;
    }

    public boolean isCheck() {
        return isCheck;
    }

    public ConsigneeInfo setIsCheck(boolean isCheck) {
        this.isCheck = isCheck;
        return this;
    }
}
