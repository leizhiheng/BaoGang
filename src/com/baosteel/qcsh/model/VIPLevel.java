package com.baosteel.qcsh.model;

import java.util.List;

/**
 * Created by jws on 2015/9/25.
 * VIP等级
 */
public class VIPLevel {
    /**
     * 等级名称
     **/
    private String gradeName;
    /**
     * 用户头像
     **/
    private String headUrl;
    /**
     * 成长值
     **/
    private String growValue;
    /**
     * 等级列表List
     **/
    private List<GradeList> gradeList;
    /**
     * 下一等级最小成长值
     **/
    private String nextGradeScore;
    /**
     * 下一等级id
     **/
    private String nextGradeId;
    /**
     * 下一等级id
     **/
    private String nextGradeName;
    /**
     * 离下一等级成长值
     **/
    private String awayNextGradeScore;

    public String getGradeName() {
        return gradeName;
    }

    public VIPLevel setGradeName(String gradeName) {
        this.gradeName = gradeName;
        return this;
    }

    public String getHeadUrl() {
        return headUrl;
    }

    public VIPLevel setHeadUrl(String headUrl) {
        this.headUrl = headUrl;
        return this;
    }

    public String getGrowValue() {
        return growValue;
    }

    public VIPLevel setGrowValue(String growValue) {
        this.growValue = growValue;
        return this;
    }

    public List<GradeList> getGradeList() {
        return gradeList;
    }

    public VIPLevel setGradeList(List<GradeList> gradeList) {
        this.gradeList = gradeList;
        return this;
    }

    public String getNextGradeScore() {
        return nextGradeScore;
    }

    public VIPLevel setNextGradeScore(String nextGradeScore) {
        this.nextGradeScore = nextGradeScore;
        return this;
    }

    public String getNextGradeId() {
        return nextGradeId;
    }

    public VIPLevel setNextGradeId(String nextGradeId) {
        this.nextGradeId = nextGradeId;
        return this;
    }

    public String getNextGradeName() {
        return nextGradeName;
    }

    public VIPLevel setNextGradeName(String nextGradeName) {
        this.nextGradeName = nextGradeName;
        return this;
    }

    public String getAwayNextGradeScore() {
        return awayNextGradeScore;
    }

    public VIPLevel setAwayNextGradeScore(String awayNextGradeScore) {
        this.awayNextGradeScore = awayNextGradeScore;
        return this;
    }
}
