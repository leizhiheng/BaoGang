package com.baosteel.qcsh.model;

import java.io.Serializable;

public class HealthyContactPerson implements Serializable {

	private static final long serialVersionUID = 1L;

	private boolean isSelected;
	private String name;
	private String phone;
	private String email;
	private String sex;
	private String identiryCode;

	public HealthyContactPerson(String name, String phone, String email) {
		this.name = name;
		this.phone = phone;
		this.email = email;
	}
	public HealthyContactPerson(String name, String sex, String phone, String indentityCode) {
		this.name = name;
		this.sex = sex;
		this.phone = phone;
		this.identiryCode = indentityCode;
	}

	public boolean isSelected() {
		return isSelected;
	}

	public void setSelected(boolean isSelected) {
		this.isSelected = isSelected;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getIdentiryCode() {
		return identiryCode;
	}

	public void setIdentiryCode(String identiryCode) {
		this.identiryCode = identiryCode;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

}
