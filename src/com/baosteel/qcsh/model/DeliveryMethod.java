public class DeliveryMethod {
    private int type;
    private int fare;
    private String mentionPoint;
    private String address;
    private String phone;
    private String date;

    public DeliveryMethod(int type, int fare, String mentionPoint, String address, String phone, String date) {
        this.type = type;
        this.fare = fare;
        this.mentionPoint = mentionPoint;
        this.address = address;
        this.phone = phone;
        this.date = date;
    }

    public int getType() {
        return type;
    }

    public DeliveryMethod setType(int type) {
        this.type = type;
        return this;
    }

    public int getFare() {
        return fare;
    }

    public DeliveryMethod setFare(int fare) {
        this.fare = fare;
        return this;
    }

    public String getMentionPoint() {
        return mentionPoint;
    }

    public DeliveryMethod setMentionPoint(String mentionPoint) {
        this.mentionPoint = mentionPoint;
        return this;
    }

    public String getAddress() {
        return address;
    }

    public DeliveryMethod setAddress(String address) {
        this.address = address;
        return this;
    }

    public String getPhone() {
        return phone;
    }

    public DeliveryMethod setPhone(String phone) {
        this.phone = phone;
        return this;
    }

    public String getDate() {
        return date;
    }

    public DeliveryMethod setDate(String date) {
        this.date = date;
        return this;
    }
}
