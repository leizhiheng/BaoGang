package com.baosteel.qcsh.model;

import com.baosteel.qcsh.utils.StringUtils;

/**
 * 
 * @description 订单列表-订单中的商品
 * @author blue
 * @Time 2015-9-28
 * 
 */
public class OrderProduct {
	// {
	// goodsId 商品编号
	// goodsImg 商品图片
	// goodsName 商品名称
	// goodsPrice 商品价格
	// goodsSn 商品内码
	// goodsSpec 商品描述
	// goodsSum 商品数量
	// goodsType 商品类型
	// numberLogistics 物流单号
	// logisticsId 物流公司ID
	// payPrice 支付价格
	// }
	private String goodsSpec;
	private String goodsId;
	private String payPrice;
	private String logisticsId;
	private String goodsImg;
	private String goodsSum;
	private String goodsPrice;//订单列表中商品的price
	private String price;//订单详情中商品的price
	private String goodsSn;
	private String numberLogistics;
	private String goodsName;
	private String goodsType;

	public String getGoodsSpec() {
		return goodsSpec;
	}

	public void setGoodsSpec(String goodsSpec) {
		this.goodsSpec = goodsSpec;
	}

	public String getGoodsId() {
		return goodsId;
	}

	public void setGoodsId(String goodsId) {
		this.goodsId = goodsId;
	}

	public String getPayPrice() {
		return payPrice;
	}

	public void setPayPrice(String payPrice) {
		this.payPrice = payPrice;
	}

	public String getLogisticsId() {
		return logisticsId;
	}

	public void setLogisticsId(String logisticsId) {
		this.logisticsId = logisticsId;
	}

	public String getGoodsImg() {
		return goodsImg;
	}

	public void setGoodsImg(String goodsImg) {
		this.goodsImg = goodsImg;
	}

	public String getGoodsSum() {
		return goodsSum;
	}

	public void setGoodsSum(String goodsSum) {
		this.goodsSum = goodsSum;
	}

	public String getGoodsPrice() {
		if (StringUtils.isEmpty(goodsPrice)) {
			goodsPrice = price;
		}
		return goodsPrice;
	}

	public void setGoodsPrice(String goodsPrice) {
		this.goodsPrice = goodsPrice;
	}

	public String getGoodsSn() {
		return goodsSn;
	}

	public void setGoodsSn(String goodsSn) {
		this.goodsSn = goodsSn;
	}

	public String getNumberLogistics() {
		return numberLogistics;
	}

	public void setNumberLogistics(String numberLogistics) {
		this.numberLogistics = numberLogistics;
	}

	public String getGoodsName() {
		return goodsName;
	}

	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}

	public String getGoodsType() {
		return goodsType;
	}

	public void setGoodsType(String goodsType) {
		this.goodsType = goodsType;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

}
