public class Business {
    /**商家Id**/
    private int id ;
    /**商家名称**/
    private String name;
    /**商家地址**/
    private String address;
    /**商家技术**/
    private String tech;
    /**商家评价数量**/
    private int commentNum;
    /**商家距离**/
    private int commentFar;

    public Business(String name, String address, String tech, int commentNum, int commentFar) {
        this.name = name;
        this.address = address;
        this.tech = tech;
        this.commentNum = commentNum;
        this.commentFar = commentFar;
    }

    public int getId() {
        return id;
    }

    public Business setId(int id) {
        this.id = id;
        return this;
    }

    public String getName() {
        return name;
    }

    public Business setName(String name) {
        this.name = name;
        return this;
    }

    public String getAddress() {
        return address;
    }

    public Business setAddress(String address) {
        this.address = address;
        return this;
    }

    public String getTech() {
        return tech;
    }

    public Business setTech(String tech) {
        this.tech = tech;
        return this;
    }


    public int getCommentNum() {
        return commentNum;
    }

    public Business setCommentNum(int commentNum) {
        this.commentNum = commentNum;
        return this;
    }

    public int getCommentFar() {
        return commentFar;
    }

    public Business setCommentFar(int commentFar) {
        this.commentFar = commentFar;
        return this;
    }
}
