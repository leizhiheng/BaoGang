public class TravelGvData {
    private int id;
    private int color;
    private boolean check;
    private String content;

    public TravelGvData(int color, boolean check, String content) {
        this.color = color;
        this.check = check;
        this.content = content;
    }

    public int getId() {
        return id;
    }

    public TravelGvData setId(int id) {
        this.id = id;
        return this;
    }

    public int getColor() {
        return color;
    }

    public TravelGvData setColor(int color) {
        this.color = color;
        return this;
    }

    public boolean isCheck() {
        return check;
    }

    public TravelGvData setCheck(boolean check) {
        this.check = check;
        return this;
    }

    public String getContent() {
        return content;
    }

    public TravelGvData setContent(String content) {
        this.content = content;
        return this;
    }
}
