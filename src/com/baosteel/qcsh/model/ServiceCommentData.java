package com.baosteel.qcsh.model;

import java.util.List;

public class ServiceCommentData {

	private String mUserName;
	private String mComment;
	private List<String> mImgUrls;
	private List<AmendComment> mAmendComments;

	public String getmUserName() {
		return mUserName;
	}

	public void setmUserName(String mUserName) {
		this.mUserName = mUserName;
	}

	public String getmComment() {
		return mComment;
	}

	public void setmComment(String mComment) {
		this.mComment = mComment;
	}

	public List<String> getmImgUrls() {
		return mImgUrls;
	}

	public void setmImgUrls(List<String> mImgUrls) {
		this.mImgUrls = mImgUrls;
	}

	public List<AmendComment> getmAmendComments() {
		return mAmendComments;
	}

	public void setmAmendComments(List<AmendComment> mAmendComments) {
		this.mAmendComments = mAmendComments;
	}
	
}
