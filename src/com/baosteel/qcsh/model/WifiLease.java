public class WifiLease {
    private String name;
    private String price;
    private int count;

    public WifiLease(String name, String price, int count) {
        this.name = name;
        this.price = price;
        this.count = count;
    }

    public String getName() {
        return name;
    }

    public WifiLease setName(String name) {
        this.name = name;
        return this;
    }

    public String getPrice() {
        return price;
    }

    public WifiLease setPrice(String price) {
        this.price = price;
        return this;
    }

    public int getCount() {
        return count;
    }

    public WifiLease setCount(int count) {
        this.count = count;
        return this;
    }
}
