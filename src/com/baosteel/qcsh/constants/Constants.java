package com.baosteel.qcsh.constants;

import android.graphics.Color;

/**
 * 常量
 * @author 刘远祺
 *
 * @todo TODO
 *
 * @date 2015-7-28
 */
public class Constants {
	
	/**舌尖宝**/
	public static final int TYPE_TONGUE 				= 1;
	
	/**旅行宝**/
	public static final int TYPE_TRAVEL 				= 2;
	
	/**乐居宝**/
	public static final int TYPE_HAPPY_LIVING 			= 3;
	
	/**健康宝**/
	public static final int TYPE_HEALTH 				= 4;
	
	/**安程宝**/
	public static final int TYPE_SAFE_TRIP 				= 5;
	
	/**欢娱宝**/
	public static final int TYPE_RECREATION 			= 6;
	
	/**趣学宝**/
	public static final int TYPE_STUDY 					= 7;
	
	/**
	 * 获取各个宝的titleBar背景颜色
	 * @param type
	 * @return
	 */
	public static final int getTypeColor(int type) {
		switch (type) {
		case TYPE_TONGUE:
			/** 舌尖宝 **/

			return Color.parseColor("#FF662A");

		case TYPE_TRAVEL:
			/** 旅行宝 **/

			return Color.parseColor("#EFC500");

		case TYPE_HAPPY_LIVING:
			/** 乐居宝 **/

			return Color.parseColor("#B674EA");

		case TYPE_STUDY:
			/** 趣学宝 **/
			
			return Color.parseColor("#52A2F4");
		

		case TYPE_SAFE_TRIP:
			/** 安程宝 **/

			return Color.parseColor("#54C151");

		case TYPE_RECREATION:
			/** 欢娱宝 **/

			return Color.parseColor("#F52F14");
			
		case TYPE_HEALTH:
			/** 健康宝 **/
			
			return Color.parseColor("#2FD1CB");
		default:
				
			/** 舌尖宝 **/
			return Color.parseColor("#FF662A");
		}
	}
}
